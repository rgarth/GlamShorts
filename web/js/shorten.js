function submitform() {
  var shortenAPI = "https://shitp.st/shorten"
  var redirectAPI = "https://shitp.st"
  longURL = $('#url').val();
  
  if (! isUrlValid(longURL)) {
    $( "#short" ).val("Error: Invalid URL");
    return;
  }
  

  $.ajax({
    type: "POST",
    url: shortenAPI,
    data: JSON.stringify({"url": longURL}),
    success: function(data, textStatus, XmlHttpRequest) {
      if (XmlHttpRequest.status === 200) {
        $( "#short" ).val(redirectAPI + "/" + data["short_url"]);
        $( "#short" ).prop( "disabled", false );
      } else {
        $( "#short" ).val("Error: Response " + XmlHttpRequest.status);
      }
    },
    error: function(XMLHttpRequest, textStatus, errorThrown) {
      $( "#short" ).val("Error: Response " + textStatus);
    },

    dataType: "json",
    contentType : "application/json"
  });
}

function copyresult() {
  $("#short").select();
  document.execCommand("Copy");
}

$(document).ready(function() {
  $('#url').keydown(function(event) {
    if (event.keyCode == 13) {
      submitform();
      return false;
    }
  });
});

